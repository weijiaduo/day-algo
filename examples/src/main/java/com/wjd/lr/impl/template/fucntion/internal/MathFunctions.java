package com.wjd.lr.impl.template.fucntion.internal;

/**
 * 系统计算函数
 *
 * @author weijiaduo
 * @since 2023/3/7
 */
public class MathFunctions {

    /**
     * Floor double.
     *
     * @param a the a
     * @return the double
     */
    public static double floor(double a) {
        return Math.floor(a);
    }

    /**
     * Ceil double.
     *
     * @param a the a
     * @return the double
     */
    public static double ceil(double a) {
        return Math.ceil(a);
    }

}
